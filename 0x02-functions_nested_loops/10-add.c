/**
 * add - adds two integers and returns the result
 * @num1: first arguments passed to be added
 * @num2: second arguments passed to be added
 * Return: returns the sum of adding two numbers
*/
int add(int num1, int num2)
{
	int sum = 0;

	sum = num1 + num2;

	return (sum);
}

